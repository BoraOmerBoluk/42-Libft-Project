#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s != (char)c && *s != '\0')
		s++;
	if (*s == '\0')
	{
		if ((char)c != '\0')
			return (NULL);
		else
			return ((char *)s);
	}
	return ((char *)s);
}
